package com.bgs.examinationbackstage.pojo;



public class TestPaperQuestions {

  private long testPaperId;
  private long testQuestionsId;
  private String testGrade;


  public long getTestPaperId() {
    return testPaperId;
  }

  public void setTestPaperId(long testPaperId) {
    this.testPaperId = testPaperId;
  }


  public long getTestQuestionsId() {
    return testQuestionsId;
  }

  public void setTestQuestionsId(long testQuestionsId) {
    this.testQuestionsId = testQuestionsId;
  }


  public String getTestGrade() {
    return testGrade;
  }

  public void setTestGrade(String testGrade) {
    this.testGrade = testGrade;
  }

}
